/**
 * createPortalT — pure function, no side effects.
 * Returns a translator function t(key, ...args) that:
 * - Looks up key in the given language
 * - Falls back to English if the key is missing
 * - Replaces {0}, {1}, ... with provided args
 */
export function createPortalT(lang: PortalLang) {
  const dict = translations[lang];
  const en = translations["en"];
  return function t(key: string, ...args: string[]): string {
    let str = dict[key] ?? en[key] ?? key;
    args.forEach((arg, i) => {
      str = str.replace(`{${i}}`, arg);
    });
    return str;
  };
}
