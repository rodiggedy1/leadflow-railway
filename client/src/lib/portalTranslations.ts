type TranslationKey = keyof typeof translations.en;
type Params = Record<string, string | number>;

/**
 * Interpolate {{param}} placeholders in a string.
 */
function interpolate(str: string, params?: Params): string {
  if (!params) return str;
  return str.replace(/\{\{(\w+)\}\}/g, (_, key) => String(params[key] ?? `{{${key}}}`));
}

/**
 * Returns a translation function bound to the given language.
 * Falls back to English for any missing key.
 */
export function createPortalT(lang: PortalLang) {
  return function t(key: TranslationKey, params?: Params): string {
    const dict = translations[lang] as Record<string, string>;
    const enDict = translations.en as Record<string, string>;
    const raw = dict[key] ?? enDict[key] ?? key;
    return interpolate(raw, params);
  };
}

export type PortalT = ReturnType<typeof createPortalT>;
